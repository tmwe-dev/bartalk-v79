const translations: Record<string, Record<string, string>> = {
  ...baseTranslations,
  ...extraTranslations,
};

// ── Hook React ────────────────────────────────────────────────────────

/**
 * Hook per ottenere la funzione di traduzione nel contesto React.
 * Legge automaticamente la lingua corrente dalle impostazioni.
 */
export function useT(): (key: string) => string {
  const { language } = useSettingsContext();
  const lang = language || 'it';
  return (key: string): string => {
    return translations[lang]?.[key] || translations['en']?.[key] || translations['it']?.[key] || key;
  };
}

// ── Funzione standalone (non-React) ──────────────────────────────────

/**
 * Crea una funzione di traduzione per una lingua specifica.
 * Usare quando non si ha accesso al contesto React (es. utils, constants).
 */
export function getT(lang: string): (key: string) => string {
  return (key: string): string => {
    return translations[lang]?.[key] || translations['en']?.[key] || translations['it']?.[key] || key;
  };
}

/**
 * Lista delle lingue con traduzione UI completa.
 */
export const FULLY_TRANSLATED_LANGS = [
  'it', 'en', 'es', 'fr', 'de', 'pt', 'zh', 'ja', 'ko', 'ru',
  'th', 'ar', 'hi', 'bn', 'tr', 'vi', 'pl', 'uk', 'el', 'cs',
  'da', 'fi', 'no', 'id', 'ms', 'tl', 'he', 'fa', 'ur', 'sw',
] as const;

/**
 * Verifica se una lingua ha traduzione UI completa.
 */
export function isFullyTranslated(lang: string): boolean {
  return (FULLY_TRANSLATED_LANGS as readonly string[]).includes(lang);
}
